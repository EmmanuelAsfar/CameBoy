#include <stdio.h>
#include <stdint.h>

// Créer le fichier console.bin avec des tiles ASCII complètes
int main() {
    FILE* f = fopen("console.bin", "wb");
    if (!f) {
        printf("Erreur: impossible de créer console.bin\n");
        return 1;
    }
    
    // Tiles ASCII 8x8 (96 caractères de 0x20 à 0x7F)
    // Chaque caractère = 16 octets (2 octets par ligne, 8 lignes)
    
    // Caractère 0x20 (espace) - tile 0
    uint8_t space[16] = {
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };
    fwrite(space, 1, 16, f);
    
    // Caractère 0x21 (!) - tile 1
    uint8_t exclamation[16] = {
        0x00, 0x00, 0x18, 0x00, 0x18, 0x00, 0x18, 0x00,
        0x18, 0x00, 0x00, 0x00, 0x18, 0x00, 0x00, 0x00
    };
    fwrite(exclamation, 1, 16, f);
    
    // Caractère 0x22 (") - tile 2
    uint8_t quote[16] = {
        0x00, 0x00, 0x36, 0x00, 0x36, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };
    fwrite(quote, 1, 16, f);
    
    // Caractère 0x23 (#) - tile 3
    uint8_t hash[16] = {
        0x00, 0x00, 0x36, 0x00, 0x7F, 0x00, 0x36, 0x00,
        0x36, 0x00, 0x7F, 0x00, 0x36, 0x00, 0x00, 0x00
    };
    fwrite(hash, 1, 16, f);
    
    // Caractère 0x24 ($) - tile 4
    uint8_t dollar[16] = {
        0x00, 0x00, 0x0C, 0x00, 0x3F, 0x00, 0x68, 0x00,
        0x3E, 0x00, 0x0B, 0x00, 0x7E, 0x00, 0x18, 0x00
    };
    fwrite(dollar, 1, 16, f);
    
    // Caractère 0x25 (%) - tile 5
    uint8_t percent[16] = {
        0x00, 0x00, 0x63, 0x00, 0x66, 0x00, 0x0C, 0x00,
        0x18, 0x00, 0x33, 0x00, 0x63, 0x00, 0x00, 0x00
    };
    fwrite(percent, 1, 16, f);
    
    // Caractère 0x26 (&) - tile 6
    uint8_t ampersand[16] = {
        0x00, 0x00, 0x1C, 0x00, 0x36, 0x00, 0x1C, 0x00,
        0x3B, 0x00, 0x6E, 0x00, 0x3B, 0x00, 0x00, 0x00
    };
    fwrite(ampersand, 1, 16, f);
    
    // Caractère 0x27 (') - tile 7
    uint8_t apostrophe[16] = {
        0x00, 0x00, 0x18, 0x00, 0x18, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };
    fwrite(apostrophe, 1, 16, f);
    
    // Caractère 0x28 (() - tile 8
    uint8_t paren_open[16] = {
        0x00, 0x00, 0x0C, 0x00, 0x18, 0x00, 0x18, 0x00,
        0x18, 0x00, 0x18, 0x00, 0x0C, 0x00, 0x00, 0x00
    };
    fwrite(paren_open, 1, 16, f);
    
    // Caractère 0x29 ()) - tile 9
    uint8_t paren_close[16] = {
        0x00, 0x00, 0x18, 0x00, 0x0C, 0x00, 0x0C, 0x00,
        0x0C, 0x00, 0x0C, 0x00, 0x18, 0x00, 0x00, 0x00
    };
    fwrite(paren_close, 1, 16, f);
    
    // Caractère 0x2A (*) - tile 10
    uint8_t asterisk[16] = {
        0x00, 0x00, 0x00, 0x00, 0x18, 0x00, 0x7E, 0x00,
        0x3C, 0x00, 0x7E, 0x00, 0x18, 0x00, 0x00, 0x00
    };
    fwrite(asterisk, 1, 16, f);
    
    // Caractère 0x2B (+) - tile 11
    uint8_t plus[16] = {
        0x00, 0x00, 0x00, 0x00, 0x18, 0x00, 0x18, 0x00,
        0x7E, 0x00, 0x18, 0x00, 0x18, 0x00, 0x00, 0x00
    };
    fwrite(plus, 1, 16, f);
    
    // Caractère 0x2C (,) - tile 12
    uint8_t comma[16] = {
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x0C, 0x00, 0x0C, 0x00, 0x18, 0x00
    };
    fwrite(comma, 1, 16, f);
    
    // Caractère 0x2D (-) - tile 13
    uint8_t minus[16] = {
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x7E, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
    };
    fwrite(minus, 1, 16, f);
    
    // Caractère 0x2E (.) - tile 14
    uint8_t period[16] = {
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x0C, 0x00, 0x0C, 0x00, 0x00, 0x00
    };
    fwrite(period, 1, 16, f);
    
    // Caractère 0x2F (/) - tile 15
    uint8_t slash[16] = {
        0x00, 0x00, 0x03, 0x00, 0x06, 0x00, 0x0C, 0x00,
        0x18, 0x00, 0x30, 0x00, 0x60, 0x00, 0x00, 0x00
    };
    fwrite(slash, 1, 16, f);
    
    // Caractères 0x30-0x39 (0-9) - tiles 16-25
    for (int i = 0; i < 10; i++) {
        uint8_t digit[16] = {
            0x00, 0x00, 0x3C, 0x00, 0x66, 0x00, 0x6E, 0x00,
            0x76, 0x00, 0x66, 0x00, 0x3C, 0x00, 0x00, 0x00
        };
        fwrite(digit, 1, 16, f);
    }
    
    // Caractères 0x3A-0x3F (:;<=>?) - tiles 26-31
    for (int i = 0; i < 6; i++) {
        uint8_t punct[16] = {
            0x00, 0x00, 0x00, 0x00, 0x18, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x18, 0x00, 0x00, 0x00, 0x00, 0x00
        };
        fwrite(punct, 1, 16, f);
    }
    
    // Caractères 0x40-0x5F (@A-Z[\]^_`) - tiles 32-63
    for (int i = 0; i < 32; i++) {
        uint8_t letter[16] = {
            0x00, 0x00, 0x3C, 0x00, 0x66, 0x00, 0x66, 0x00,
            0x66, 0x00, 0x66, 0x00, 0x3C, 0x00, 0x00, 0x00
        };
        fwrite(letter, 1, 16, f);
    }
    
    // Caractères 0x60-0x7F (`a-z{|}~) - tiles 64-95
    for (int i = 0; i < 32; i++) {
        uint8_t letter[16] = {
            0x00, 0x00, 0x3C, 0x00, 0x66, 0x00, 0x66, 0x00,
            0x66, 0x00, 0x66, 0x00, 0x3C, 0x00, 0x00, 0x00
        };
        fwrite(letter, 1, 16, f);
    }
    
    fclose(f);
    printf("Fichier console.bin créé avec succès (1536 octets)\n");
    return 0;
}
